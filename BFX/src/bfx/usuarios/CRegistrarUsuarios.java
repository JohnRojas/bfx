    private void objTFnombreDeUsuarioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_objTFnombreDeUsuarioKeyPressed
        if (evt.getKeyChar() == '\n')
        {
            if ("".equals(objTFnombreDeUsuario.getText()))
            {
                CMensajes.cmetpbFalla(this);
                objTFnombreDeUsuario.requestFocus();
            } else
            {
                if (pbALusuarios==null)
                {
                    CManipulaInterfaz.cmetpbCambia(objTFcontrasenia);
                }else
                {
                     for (int lEi=0; lEi<pbALusuarios.size(); lEi++)
                    {
                        String lSusername=objTFnombreDeUsuario.getText().trim();

                        if(pbALusuarios.get(lEi).getpbSNombreDeUsuario().trim().equals(lSusername))
                        {
                            CMensajes.cmetpbFalla(this,"El sobrenombre de usuario ya existe, intenta con otro");
                            objTFnombreDeUsuario.requestFocus();
                            objTFnombreDeUsuario.setText("");
                            lEi=pbALusuarios.size();
                        } else
                        {
                            if(lEi==pbALusuarios.size()-1)
                            {
                                CManipulaInterfaz.cmetpbCambia(objTFcontrasenia);
                            }
                        }
                    }
                }
               
            }
        }
    }//GEN-LAST:event_objTFnombreDeUsuarioKeyPressed

